private void pesquisar() {
        //pega o modelo da grade com suas colunas
        DefaultTableModel modeloTabela = (DefaultTableModel) tblServicos.getModel();

        //limpa a grade setando o numero de linhas para zero
        modeloTabela.setNumRows(0);

        int opcaoFiltro = cbxFiltro.getSelectedIndex();
        String filtro = edtFiltro.getText();

        //consultar banco de dados
       ServicoController controler = new ServicoController();

        List<Servico> listaServicos = controler.consultar(opcaoFiltro, filtro);

        //preencher a grade
        for (Servico ser : listaServicos) {
            Object[] linha = {
                ser.getIdServico(), //0
                ser.getNomeServico(), //1
                ser.getDescricao(), //2
                ser.getValorUnitario() //3
            };

            modeloTabela.addRow(linha);

        }
    }
