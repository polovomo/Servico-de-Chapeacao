    private void pesquisar() {
        //pega o modelo da grade com suas colunas
        DefaultTableModel modeloTabela = (DefaultTableModel) tblOrdens.getModel();

        //limpa a grade setando o numero de linhas para zero
        modeloTabela.setNumRows(0);

        int opcaoFiltro = cbxFiltro.getSelectedIndex();
        String filtro = edtFiltro.getText();

        //consultar banco de dados
        OsController controler = new  OsController();

        List<OrdemServico> listaOrdens = controler.consultar(opcaoFiltro, filtro);

        //preencher a grade
        for (OrdemServico os : listaOrdens) {
            Object[] linha = {
                os.getIdOrdemServico(), //0
                os.getNomeCliente(), //1
                os.getModeloCarrro(), //2
                os.getPlacaCarro()//3
            };

            modeloTabela.addRow(linha);

        }
    }
